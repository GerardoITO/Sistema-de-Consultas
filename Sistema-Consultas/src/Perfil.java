    public Object [][] getDatos(){
      int registros = 0;
      //obtenemos la cantidad de registros existentes en la tabla
      try{         
         PreparedStatement pstm = con.getConnection().prepareStatement("SELECT count(id_consulta) as total FROM consulta ");
         ResultSet res = pstm.executeQuery();
         res.next();
         registros = res.getInt("total");
         res.close();
      }catch(SQLException e){
         System.out.println(e);
      }    
       
      Object[][] data = new String[registros][7];  
    //realizamos la consulta sql y llenamos los datos en "Object"
      try{    
         PreparedStatement pstm = con.getConnection().prepareStatement("SELECT " +
            " id_consulta, folio_seguropopular, rfc_doctor, tipo_consulta, turno, fecha, hora" +
            " FROM consulta" +
            " ORDER BY id_consulta ");
         ResultSet res = pstm.executeQuery();
         int i = 0;
         while(res.next()){
            String id = res.getString("id_consulta");
            String folio = res.getString("folio_seguropopular");
            String rfc = res.getString("rfc_doctor");
            String tipo = res.getString("tipo_consulta");
            String turno = res.getString("turno");
            String fecha = res.getString("fecha");
            String hora = res.getString("hora");
            data[i][0] = id;            
            data[i][1] = folio;            
            data[i][2] = rfc;            
            data[i][3] = tipo;   
            data[i][4] = turno;    
            data[i][5] = fecha;    
            data[i][6] = hora;  
            i++;
         }
         res.close();
          }catch(SQLException e){
         System.out.println(e);
    }
    return data;
 }
