    private void updateTabla(){             
        String[] columNames = {"ID_CONSULTA","FOLIO SEG POP","RFC DOCTOR","TIPO CONSULTA","TURNO","FECHA","HORA"};  
        // se utiliza la funcion
        dtPac = getDatos();
        // se colocan los datos en la tabla
        DefaultTableModel datos = new DefaultTableModel(dtPac,columNames);                        
        tabla.setModel(datos); 
    }
    public void rellenar(String folio){  
            try {                
                PreparedStatement pstm = con.getConnection().prepareStatement("select folio_seguropopular, nombre, apellido_pat, apellido_mat, municipio, localidad, telefono from paciente where folio_seguropopular = ?");            
                pstm.setString(1, folio);                   
                ResultSet res = pstm.executeQuery();
                res.next();
                jLabel9.setText(res.getString("folio_seguropopular"));
                jLabel10.setText(res.getString("nombre"));
                jLabel11.setText(res.getString("apellido_pat"));
                jLabel12.setText(res.getString("apellido_mat"));
                jLabel13.setText(res.getString("municipio"));
                jLabel14.setText(res.getString("localidad"));
                jLabel15.setText(res.getString("telefono"));
                res.close();            
            }catch(SQLException e){
            System.out.println(e);
            }            
   }
